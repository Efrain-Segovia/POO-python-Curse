# if simple
x = 5
if x > 5:
    print("x es mayor que 5")
elif x == 5:
    print("x es igual que 5")
else:
    print("x es menor que 5")


# if-else
y = 3
if y % 2 == 0:
    print("y es par")
else:
    print("y es impar")

# if-elif-else
z = 0
if z > 0:
    print("z es positivo")
elif z < 0:
    print("z es negativo")
else:
    print("z es cero")