#bucle for
for contador in range(5):
    print("El valor de i es:", contador)

#range con inicio y fin
for i in range(2, 5):
    print("El valor de i es:", i)

#range con paso
for i in range(1, 10, 2):
    print("El valor de i es:", i)

#bucle for con else
for j in range(3):
    print("El valor de j es:", j)
else:
    print("El bucle ha terminado.")

#bucle for con lista
fruits = ["manzana", "banana", "cereza"]
for fruit in fruits:
    print("La fruta es:", fruit)

